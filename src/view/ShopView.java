    public void openCashView() {
        new CashView(this, true, shop).setVisible(true);
    }

    public void openProductView(int option) {
        // 'option' indica a ProductView qué debe hacer (ańadir, stock o borrar)
        new ProductView(this, true, shop, option).setVisible(true);
    }

    // --- MANEJO DE EVENTOS DE ACCIÓN (BOTONES) ---
    @Override
    public void actionPerformed(ActionEvent e) {
        if (e.getSource() == btnCash) {
            openCashView();
        } else if (e.getSource() == btnAddProduct) {
            openProductView(2);
        } else if (e.getSource() == btnAddStock) {
            openProductView(3);
        } else if (e.getSource() == btnDeleteProduct) {
            openProductView(9);
        } else if (e.getSource() == btnExport) {
            handleExport();
        }
        this.requestFocus();
    }

    // --- MANEJO DE EVENTOS DE TECLADO ---
    @Override
    public void keyPressed(KeyEvent e) {
        int k = e.getKeyCode(); // Requisito 3.f [cite: 169]
        if (k == KeyEvent.VK_1) {
            openCashView();
        } else if (k == KeyEvent.VK_2) {
            openProductView(2);
        } else if (k == KeyEvent.VK_3) {
            openProductView(3);
        } else if (k == KeyEvent.VK_9) {
            openProductView(9);
        } else if (k == KeyEvent.VK_7 || k == KeyEvent.VK_NUMPAD7) {
            handleExport(); 
        }
    }

    private void handleExport() {
        int r = JOptionPane.showConfirmDialog(this, "żDesea exportar las ventas?"); // [cite: 14, 148]
        if (r == JOptionPane.YES_OPTION) {
            shop.writeSales();
        }
    }

